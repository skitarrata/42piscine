int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int cont;

	i = 0;
	cont = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			cont++;
		i++;
	}
	return (cont);
}
