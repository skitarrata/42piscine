int	sum(int op1, int op2)
{
	return (op1 + op2);
}

int	min(int op1, int op2)
{
	return (op1 - op2);
}

int	div(int op1, int op2)
{
	return (op1 / op2);
}

int	mod(int op1, int op2)
{
	return (op1 % op2);
}

int	mol(int op1, int op2)
{
	return (op1 * op2);
}
